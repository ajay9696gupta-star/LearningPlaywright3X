// && -> And operator - The && operator is used to combine two or more conditions. It returns true if all the conditions are true, otherwise it returns false.
// || -> Or operator - The || operator is used to combine two or more conditions. It returns true if at least one of the conditions is true, otherwise it returns false.    


let a = true;
let b = false;

console.log(a && b); // Output: false
console.log(a || b); // Output: true
console.log(!a); // Output: false