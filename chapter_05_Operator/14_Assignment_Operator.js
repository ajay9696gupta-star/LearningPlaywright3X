// Assignment Operators
// Assignment operators are used to assign values to variables. The most common assignment operator is the equal sign (=). However, there are several other assignment operators that can be used in JavaScript. Here are some of the most commonly used assignment operators:

let x = 10; // Assigns the value 10 to the variable x
x = "Ajay"; // Assigns the string "Ajay" to the variable x
console.log(x);

let x1 =10;
x1 += 5; // x1 = x1 + 5;
console.log(x1); // Output: 15
x1 -= 3; // x1 = x1 - 3;
console.log(x1); // Output: 12
x1 *= 2; // x1 = x1 * 2;
console.log(x1); // Output: 24
x1 /= 4; // x1 = x1 / 4;
console.log(x1); // Output: 6   

