// ternary (conditional) operator
// The ternary operator is a shorthand way of writing an if-else statement. It takes three operands: a condition, a value to return if the condition is true, and a value to return if the condition is false. The syntax for the ternary operator is as follows:

let age = 15;
let isAdult = age >= 18 ? "Yes" : "No";
console.log(isAdult);
