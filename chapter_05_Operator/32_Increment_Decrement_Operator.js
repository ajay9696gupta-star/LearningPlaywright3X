// pre-Increment

let a = 10;
let b = ++a; // pre-increment: increment a by 1, then assign to b
console.log(`a: ${a}, b: ${b}`); // Output: a: 11, b: 11    

// [post-Increment]
let c = 10;
let d = c++; // post-increment: assign c to d, then increment c by 1
console.log(`c: ${c}, d: ${d}`); // Output: c: 11, d: 10