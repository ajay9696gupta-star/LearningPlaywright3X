let count = 42; 
console.log(count); // Output: 42
console.log(typeof count); // Output: number

let negativeCount = -42;
console.log(negativeCount); // Output: -42

let zeroCount = 0;
console.log(zeroCount); // Output: 0

let h = 0xFF; // hexadecimal literal
console.log(h); // Output: 255
console.log(typeof h); // Output: number

let octal   = 0o377; // octal literal
console.log(octal); // Output: 255      
console.log(typeof octal); // Output: number