let decimal = 3.14; // decimal literal
console.log(decimal);   
console.log(typeof decimal); // Output: number

let binary = 0b1010; // binary literal
console.log(binary);   
console.log(typeof binary); // Output: number

let octal = 0o12; // octal literal
console.log(octal);   
console.log(typeof octal); // Output: number    

let hexadecimal = 0xA; // hexadecimal literal
console.log(hexadecimal);   
console.log(typeof hexadecimal); // Output: number  

let floatingPoint = 3.14; // floating-point literal
console.log(floatingPoint);   
console.log(typeof floatingPoint); // Output: number    

let floatingPointNegative = -3.14; // negative floating-point literal
console.log(floatingPointNegative);   
console.log(typeof floatingPointNegative); // Output: number    

let float2 = .5; // floating-point literal without leading digit
console.log(float2);   
console.log(typeof float2); // Output: number   


let flot3 = 5.; // floating-point literal without trailing digit
console.log(flot3);   
console.log(typeof flot3); // Output: number

let exponential = 1.5e3; // exponential literal
console.log(exponential);   
console.log(typeof exponential); // Output: number




